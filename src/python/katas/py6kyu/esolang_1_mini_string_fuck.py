def my_first_interpreter(code):
    out = ''
    cell = 0

    for ins in code:
        if ins == '+':
            cell = (cell + 1) % 256

        elif ins == '.':
            out += chr(cell)

    return ''.join(out)


def test_my_first_interpreter():
    assert my_first_interpreter(
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++'
        '+++.+++++++++++++++++++++++++++++.+++++++..+++.++++++++++++++++++++++'
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++'
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++'
        '+++++++++++++++++++++++++++++.+++++++++++++++++++++++++++++++++++++++'
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++'
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++'
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+'
        '++++++++++++++++++++++++++++++++++++++++++++++++++++++.++++++++++++++'
        '++++++++++.+++.++++++++++++++++++++++++++++++++++++++++++++++++++++++'
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++'
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++'
        '++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.++++++++++'
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++'
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++'
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++'
        '+++++++++++++++++++++++++++++++.+++++++++++++++++++++++++++++++++++++'
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++'
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++'
        '++++++++++++++.'
    ) == 'Hello, World!'

    assert my_first_interpreter(
        '+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+.+'
        '.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.'
    ) == 'ABCDEFGHIJKLMNOPQRSTUVWXYZ'
