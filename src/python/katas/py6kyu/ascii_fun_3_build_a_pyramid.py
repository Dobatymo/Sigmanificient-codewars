def build_pyramid(s: str, n: int) -> str:
    out = [
        ''.join(k * i for k in s)
        for i in range(1, n + 1)
    ]

    max_len = len(out[-1])
    return '\n'.join(
        line.center(max_len).rstrip()
        for line in out
    )


def test_build_pyramid():
    assert build_pyramid("00-00..00-00", 3) == (
        "            00-00..00-00\n"
        "      0000--0000....0000--0000\n"
        "000000---000000......000000---000000"
    )

    assert build_pyramid("00-00..00-00", 7) == (
        "                                    00-00..00-00"
        "\n                              0000--0000....0000--0000"
        "\n                        000000---000000......000000---000000"
        "\n                  00000000----00000000........00000000----00000000"
        "\n            0000000000-----0000000000..........0000000000-----0000000000"
        "\n      000000000000------000000000000............000000000000------000000000000"
        "\n00000000000000-------00000000000000..............00000000000000-------00000000000000"
    )
